// Quiz generation prompt builder - JSON format

export function buildQuizPrompt(params: {
  category: string
  count: number
  dateInfo: string
  userInfo: string
  tweetContext: string
}): string {
  const { category, count, dateInfo, userInfo, tweetContext } = params
  const categoryDesc = category === 'people' ? '特定の人物について' : '全般'
  
  return `あなたはヒカマニ・ヒカマー界隈のクイズ出題者です。
以下のツイートデータを参考に、${categoryDesc}に関するクイズを${count}問作成してください。

${dateInfo}
${userInfo}

【ツイートデータ】
${tweetContext.slice(0, 800000)}

【出力形式】1行に1つのJSONオブジェクト（JSONL形式）

形式一覧と必須フィールド:

choice4 (4択):
{"t":"choice4","q":"質問","c":["A","B","C","D"],"a":0,"e":"解説"}
※ a=正解のインデックス(0-3)

tf (○×):
{"t":"tf","q":"質問","a":true,"e":"解説"}
※ a=true(○) or false(×)

fillin (穴埋め):
{"t":"fillin","q":"___を含む文","a":"正解","alt":["許容回答"],"e":"解説"}

order (並べ替え):
{"t":"order","q":"質問","items":["項目1","項目2","項目3","項目4"],"a":[2,0,3,1],"e":"解説"}
※ a=正しい順序のインデックス配列

match (マッチング):
{"t":"match","q":"質問","left":["左1","左2","左3"],"right":["右1","右2","右3"],"a":[0,1,2],"e":"解説"}
※ a=左[i]に対応する右のインデックス

multi (複数選択):
{"t":"multi","q":"質問","c":["選択肢1","選択肢2","選択肢3","選択肢4"],"a":[0,2],"e":"解説"}
※ a=正解のインデックス配列(2つ以上)

assoc (連想):
{"t":"assoc","a":"答え","hints":["ヒント1","ヒント2","ヒント3"],"e":"解説"}

number (数値入力):
{"t":"number","q":"質問","a":42,"tol":5,"e":"解説"}
※ tol=許容範囲

category (分類):
{"t":"category","q":"質問","catA":"カテゴリA","catB":"カテゴリB","items":[{"n":"項目1","c":"A"},{"n":"項目2","c":"B"}],"e":"解説"}

speed (早押し):
{"t":"speed","a":"答え","text":"徐々に表示される文章","e":"解説"}

mistake (間違い探し):
{"t":"mistake","q":"間違いを含む文","wrong":"間違い部分","correct":"正しい内容","e":"解説"}

rank (ランキング):
{"t":"rank","q":"質問","items":["項目1","項目2","項目3","項目4"],"a":[2,0,3,1],"e":"解説"}

fillmulti (穴埋め複数):
{"t":"fillmulti","q":"___と___を含む文","a":["正解1","正解2"],"e":"解説"}

timeline (タイムライン):
{"t":"timeline","q":"出来事","a":"2024-12","tol":1,"e":"解説"}

quote (引用元当て):
{"t":"quote","q":"ツイート本文","c":["選択肢1","選択肢2","選択肢3","選択肢4"],"a":0,"e":"解説"}

hashtag (タグ当て):
{"t":"hashtag","q":"ツイート本文","a":"正解タグ","dummy":["ダミー1","ダミー2","ダミー3"],"e":"解説"}

reply (リプ先当て):
{"t":"reply","q":"リプライ本文","c":["選択肢1","選択肢2","選択肢3","選択肢4"],"a":0,"e":"解説"}

followers (フォロワー数):
{"t":"followers","q":"ユーザー名","a":1000,"tol":100,"e":"解説"}

crossword (クロスワード):
{"t":"crossword","h1":"ヒント1","a1":"答え1","h2":"ヒント2","a2":"答え2","common":"共通文字","e":"解説"}

anagram (アナグラム):
{"t":"anagram","shuffled":"シャッフル文字","a":"正解","hint":"ヒント","e":"解説"}

fillselect (穴埋め選択):
{"t":"fillselect","q":"___を含む文","c":["選択肢1","選択肢2","選択肢3","選択肢4"],"a":0,"e":"解説"}

compare (比較):
{"t":"compare","q":"質問","itemA":"A項目","itemB":"B項目","a":"A","e":"解説"}

graph (グラフ読み取り):
{"t":"graph","q":"グラフから読み取る数値の質問","data":[{"l":"ラベル1","v":10},{"l":"ラベル2","v":20}],"a":20,"tol":5,"e":"解説"}
※ グラフを見て数値を答える問題。「誰が」「何が」を聞く問題には使わない。a=正解の数値

calc (計算):
{"t":"calc","q":"質問","a":42,"tol":0,"e":"解説"}

memory (神経衰弱):
{"t":"memory","q":"質問","pairs":[{"l":"左1","r":"右1"},{"l":"左2","r":"右2"}],"e":"解説"}

deduct (推理):
{"t":"deduct","situation":"状況","hints":["ヒント1","ヒント2"],"q":"質問","c":["選択肢1","選択肢2","選択肢3","選択肢4"],"a":0,"e":"解説"}

dragfill (ドラッグ穴埋め):
{"t":"dragfill","q":"___と___を含む文","a":["正解1","正解2"],"dummy":["ダミー1","ダミー2"],"e":"解説"}

related (関連語):
{"t":"related","keyword":"キーワード","correct":["関連1","関連2","関連3"],"wrong":["非関連1","非関連2"],"e":"解説"}

oddone (仲間はずれ):
{"t":"oddone","q":"質問","items":["仲間1","仲間2","仲間3","仲間はずれ"],"a":3,"e":"解説"}

complete (文章完成):
{"t":"complete","q":"文の前半","a":"正解","alt":["許容回答"],"e":"解説"}

reverse (逆引き):
{"t":"reverse","a":"答え","c":["正解問題","ダミー1","ダミー2","ダミー3"],"correct":0,"e":"解説"}

【ルール】
1. 1行に1つの有効なJSONオブジェクトを出力（余計なテキストは書かない）
2. ツイートデータに基づいた具体的な事実から問題を作成（プレースホルダーは使わない）
3. 様々な形式をバランスよく混ぜる
4. 解説(e)は簡潔に
5. 【最重要】ヒカマニ・ヒカマー界隈に直接関係する内容のみ出題:
   - OK: ヒカキン、ヒカマニ、界隈の有名ユーザー、界隈特有のミーム・ネタ
   - NG: ツイ廃界隈の出来事
   - 「ヒカマニ」「ヒカマー」「ヒカキン」に言及しているツイートを優先
6. 界隈のユーザー、ミーム、内輪ネタ、コミュニティ内の出来事に焦点を当てる
7. 【重要】問題文・選択肢・解説に内部データ形式を絶対に含めない:
   - NG: 「U:665」「U:123」「[番号]」「ツイート[214]」
   - OK: 「ユーザー名」「@handle」「〇〇さん」
   - ユーザー番号は必ずユーザー名に変換すること
8. 【重要】問題文と解説の整合性:
   - 問題で言及したユーザーと解説で言及するユーザーは必ず同一人物
   - 別人の情報を混同しない（似た名前のユーザーに注意）
9. 【重要】一般常識で答えられる問題は禁止:
   - NG: ヒカキン・セイキンの関係、YouTubeの基本情報など誰でも知ってること
   - OK: 界隈内でしか知られていない情報、ツイートデータから得られる具体的な事実
10. 【重要】多様なユーザーから出題する:
   - 同じユーザーの問題が連続しないようにする
   - 1人のユーザーからは最大2〜3問まで
   - 様々な界隈メンバーの出来事をバランスよく出題
11. 【重要】出来事・トピックの多様性:
   - 同じ出来事・イベントから複数問題を作らない
   - 「ヒカマーズ参院選」「凍結報告」「〇〇ランキング」など特定トピックは1問まで
   - 様々な日付、様々な話題から幅広く出題する
   - ツイートデータ全体を見渡して、異なる出来事を選ぶ
12. 【重要】ユーザー名とIDの整合性:
   - 表示名（user_name）とID（@handle）は必ず同一人物のものを使う
   - 別人の表示名とIDを混同しない（例: 「づぃゆ_mania（@Neo_NaziHoshin）」はNG）
   - ツイートデータで確認できる正しい組み合わせのみ使用する

【形式別の注意】
4択(choice4):
- 不正解の選択肢も界隈に関連した具体的な内容にする
- 「教育のため」「研究のため」「芸術のため」のような汎用的な選択肢は禁止
- 不正解の選択肢は実在する界隈のユーザー名、ミーム、出来事などから作る
- 正解と不正解の選択肢の長さや具体性を揃える（正解だけ詳しいのはNG）
- 【重要】選択肢に重複を含めない（同じ名前・同じ内容を2回以上使わない）

○×(tf):
- ○と×を半々で作る（×が答えの問題を積極的に作る）
- 【重要】以下の形式は○が推測されやすいので禁止:
  - 「〇〇を投稿した」「〇〇があった」「〇〇が発見された」「〇〇と発言した」
  - 事実をそのまま述べる肯定文全般
- ×の問題の作り方:
  - 日付を1日ずらす（例:「12月28日」→「12月27日」）
  - 数字を微妙に変える（例:「100回」→「50回」）
  - 人物名を入れ替える（例:「Aが言った」→「Bが言った」）
- 問題文の形式を統一しない（「〇〇は△△である」「〇〇は△△だった」など様々な形式を使う）
- 「〇〇ではない」「〇〇しなかった」のような否定形の問題も作る

並べ替え(order)/ランキング(rank):
- 項目に日付・数字・順位を含めない（答えがバレるため）
- 選択肢や項目は答えが推測できない形式にする
- NG: 「810, 1919, 4545, 114514を小さい順に」（数字を見れば分かる）
- OK: 「〇〇のいいね数が多い順に並べよ」（項目は出来事やユーザー名）
- ランキング問題は数値そのものではなく、何かの順位を当てる問題にする

複数選択(multi):
- 正解は必ず2つ以上にする

グラフ(graph):
- 「グラフの中で最大の値は？」「合計は？」など数値を答える問題にのみ使用
- 「誰が最も多い？」「どれが1位？」のような問題はchoice4を使う

比較(compare):
- 選択肢に答えのヒントを含めない
- NG: 「低学歴であるのはどちら？」→選択肢「チヌ」「低学歴版チヌ」（答えバレバレ）
- OK: 両方の選択肢を同じ形式・長さにする（例:「チヌ」「ヒカマニ太郎」）

クロスワード(crossword):
- 答えはツイートデータに実際に登場する言葉のみ使用
- 造語や略語を勝手に作らない（例:「マニア」→「マニイ」はNG）
- 共通点(common)は必ず具体的な文字を指定する

穴埋め(fillin)/文章完成(complete):
- 答えが推測可能な問題にする
- NG: 「〇〇が最初に言った言葉は？」（知らないと答えられない）
- OK: 「〇〇の名前の由来は『ヒカキン』と『___』を組み合わせたもの」（文脈から推測可能）
- 有名なフレーズ、定番のネタ、広く知られた事実を出題する

穴埋め複数(fillmulti):
- 空欄は必ず異なる答えにする（表記ゆれや同じ言葉の繰り返しはNG）
- NG: 「雄豚」と「おすぶた」（同じ言葉）
- OK: 「ヒカキン」と「セイキン」（異なる言葉）
- 1つの答えで済む場合はfillinを使う
- 【重要】一般常識で答えられる問題は禁止
- NG: 「YouTubeテーマソング2は___と___の兄弟ユニット」（ヒカキン・セイキンは誰でも知ってる）
- OK: 界隈内でしか知られていない情報を問う問題

連想(assoc):
- ヒントは全てツイートデータに実際に存在する事実のみ使用
- 事実と異なる情報や推測を含めない
- 答えとヒントの関連性がツイートで確認できること

仲間はずれ(oddone):
- 問題文と正解の整合性を必ず確認する
- 「肯定的なものはどれ？」→正解は肯定的なもの
- 「仲間はずれはどれ？」→正解は他と異なるもの
- 問題の意図と正解が矛盾しないこと

分類(category):
- 界隈の知識がないと分類できない問題にする
- NG: 「体調不良」vs「ゲーム報告」（内容を読めば誰でも分かる）
- OK: 「〇〇派」vs「△△派」（界隈の派閥や立場を知らないと分からない）
- 分類基準が界隈特有のものであること

間違い探し(mistake):
- 明確に事実と異なる間違いを含める
- NG: 「8月13日のまとめを8月14日に公開」（毎日投稿なら正常な運用）
- OK: 「〇〇が△△と発言した」→実際は別の人が発言（人物の間違い）
- 間違いが本当に間違いであることを確認する
${dateInfo ? '\n指定された期間の出来事に関する問題を出題すること。' : ''}

${count}問を出力してください。`
}
